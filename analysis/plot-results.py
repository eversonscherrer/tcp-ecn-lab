#!/usr/bin/env python3
"""Generate a 4-panel comparison chart for the AccECN experiment.

Usage:
    python3 analysis/plot-results.py [results/] [--date YYYYMMDD] [--output path]

Saves results/comparison.png. Requires the summary.csv generated by
parse-results.py (which must include the ecn_mark and pkt_dropped columns).
"""

import argparse
import csv
import json
import sys
from collections import defaultdict
from pathlib import Path
from typing import Optional

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.gridspec as gridspec
import numpy as np

MODES = ["none", "classic", "accecn", "dctcp"]
COLORS = {"none": "#d62728", "classic": "#ff7f0e", "accecn": "#2ca02c", "dctcp": "#1f77b4"}
LABELS = {"none": "No ECN", "classic": "Classic ECN", "accecn": "AccECN", "dctcp": "DCTCP+AccECN"}
MIN_DURATION = 50  # ignore the short warm-up runs


def load_summary(results_dir: Path) -> list[dict]:
    path = results_dir / "summary.csv"
    if not path.exists():
        sys.exit(f"summary.csv not found in {results_dir}. Run parse-results.py first.")
    with path.open() as f:
        return list(csv.DictReader(f))


def load_iperf_intervals(results_dir: Path, timestamp: str, mode: str) -> list[float]:
    p = results_dir / f"{timestamp}-{mode}" / "iperf-client.json"
    if not p.exists():
        return []
    with p.open() as f:
        data = json.load(f)
    return [
        iv["sum"]["bits_per_second"] / 1e6
        for iv in data.get("intervals", [])
        if not iv.get("sum", {}).get("omitted", False) and iv.get("sum", {}).get("bits_per_second", 0) > 0
    ]


def load_server_ss_cwnd(results_dir: Path, timestamp: str, mode: str) -> list[tuple[float, int]]:
    """Return [(relative_time_s, cwnd), ...] from server-ss.log if present."""
    import re
    p = results_dir / f"{timestamp}-{mode}" / "server-ss.log"
    if not p.exists():
        return []
    samples: list[tuple[float, int]] = []
    t0 = None
    for line in p.read_text().splitlines():
        parts = line.split()
        if not parts:
            continue
        try:
            ts = float(parts[0])
        except ValueError:
            continue
        m = re.search(r"\bcwnd:(\d+)\b", line)
        if m:
            if t0 is None:
                t0 = ts
            samples.append((ts - t0, int(m.group(1))))
    return samples


def group_into_sessions(rows: list[dict]) -> list[dict[str, dict]]:
    """Group consecutive runs into sessions starting at each 'none' entry.

    Accepts sessions with 3 modes (none/classic/accecn) or 4 (+ dctcp).
    """
    VALID = ({"none", "classic", "accecn"}, {"none", "classic", "accecn", "dctcp"})
    sessions: list[dict[str, dict]] = []
    current: dict[str, dict] = {}
    for row in rows:
        mode = row["mode"]
        if mode == "none" and current:
            if set(current) in VALID:
                sessions.append(current)
            current = {}
        current[mode] = row
    if set(current) in VALID:
        sessions.append(current)
    return sessions


def plot_comparison(results_dir: Path, output: Path, date_filter: Optional[str] = None) -> None:
    all_rows = load_summary(results_dir)
    if date_filter:
        all_rows = [r for r in all_rows if r.get("timestamp", "").startswith(date_filter)]
        if not all_rows:
            sys.exit(f"No rows found for date {date_filter} in {results_dir / 'summary.csv'}")

    rows = [r for r in all_rows if float(r.get("duration_s", 0)) >= MIN_DURATION]
    if not rows:
        rows = all_rows

    by_mode: dict[str, list[dict]] = defaultdict(list)
    for r in rows:
        by_mode[r["mode"]].append(r)

    # Only plot modes present in data, preserving canonical order
    active_modes = [m for m in MODES if m in by_mode]

    sessions = group_into_sessions(rows)
    latest = sessions[-1] if sessions else None

    title_prefix = "TCP Congestion Control Comparison"
    if date_filter:
        title_prefix += f" ({date_filter})"
    title = title_prefix + " — " + "  vs  ".join(LABELS[m] for m in active_modes)
    fig = plt.figure(figsize=(14, 10))
    fig.suptitle(title, fontsize=13, fontweight="bold", y=0.98)
    gs = gridspec.GridSpec(2, 2, figure=fig, hspace=0.42, wspace=0.32)
    ax_tput = fig.add_subplot(gs[0, 0])
    ax_ts   = fig.add_subplot(gs[0, 1])
    ax_ecn  = fig.add_subplot(gs[1, 0])
    ax_cwnd = fig.add_subplot(gs[1, 1])

    x = np.arange(len(active_modes))
    bar_colors = [COLORS[m] for m in active_modes]
    xlabels = [LABELS[m] for m in active_modes]

    # ── 1. Mean throughput (receive) per mode ─────────────────────────────
    means, stds = [], []
    for m in active_modes:
        vals = [float(r["throughput_recv_mbps"]) for r in by_mode.get(m, []) if r.get("throughput_recv_mbps")]
        means.append(np.mean(vals) if vals else 0.0)
        stds.append(np.std(vals) if len(vals) > 1 else 0.0)

    bars = ax_tput.bar(x, means, yerr=stds, capsize=6, color=bar_colors, alpha=0.85, zorder=3)
    ax_tput.set_xticks(x); ax_tput.set_xticklabels(xlabels)
    ax_tput.set_ylabel("Throughput (Mbps)")
    ax_tput.set_title("Mean Receive Throughput")
    ax_tput.set_ylim(0, max(means) * 1.25 + 2)
    ax_tput.grid(axis="y", linestyle="--", alpha=0.4, zorder=0)
    for bar, mean, std in zip(bars, means, stds):
        ax_tput.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height() + std + 1,
            f"{mean:.1f}",
            ha="center", va="bottom", fontsize=9,
        )

    # ── 2. Throughput over time — latest complete session ─────────────────
    if latest:
        for m in active_modes:
            row = latest.get(m)
            if not row:
                continue
            ts_data = load_iperf_intervals(results_dir, row["timestamp"], m)
            if ts_data:
                ax_ts.plot(range(len(ts_data)), ts_data, label=LABELS[m],
                           color=COLORS[m], linewidth=1.8, alpha=0.9)
        ax_ts.set_xlabel("Time (s)")
        ax_ts.set_ylabel("Throughput (Mbps)")
        ax_ts.set_title("Throughput Over Time (latest session)")
        ax_ts.legend(fontsize=8, loc="lower right")
        ax_ts.set_ylim(bottom=0)
        ax_ts.grid(linestyle="--", alpha=0.4)
    else:
        ax_ts.text(0.5, 0.5, "No complete session found", transform=ax_ts.transAxes,
                   ha="center", va="center", color="gray")
        ax_ts.set_title("Throughput Over Time")

    # ── 3. ECN marks vs packet drops per mode ────────────────────────────
    mark_vals: dict[str, list[int]] = defaultdict(list)
    drop_vals: dict[str, list[int]] = defaultdict(list)
    for r in rows:
        m = r["mode"]
        if r.get("ecn_mark") not in ("", None):
            mark_vals[m].append(int(r["ecn_mark"]))
        if r.get("pkt_dropped") not in ("", None):
            drop_vals[m].append(int(r["pkt_dropped"]))

    has_qdisc_data = any(mark_vals.values()) or any(drop_vals.values())
    if has_qdisc_data:
        w = 0.35
        mark_means = [np.mean(mark_vals[m]) if mark_vals[m] else 0 for m in active_modes]
        drop_means = [np.mean(drop_vals[m]) if drop_vals[m] else 0 for m in active_modes]
        bars1 = ax_ecn.bar(x - w / 2, mark_means, w, label="ECN marks (fq_codel)",
                           color="#1f77b4", alpha=0.85, zorder=3)
        bars2 = ax_ecn.bar(x + w / 2, drop_means, w, label="Packet drops (fq_codel)",
                           color="#d62728", alpha=0.85, zorder=3)
        ax_ecn.set_xticks(x); ax_ecn.set_xticklabels(xlabels)
        ax_ecn.set_ylabel("Packets (mean per run)")
        ax_ecn.set_title("Congestion Signal: Marks vs Drops")
        ax_ecn.legend(fontsize=8)
        ax_ecn.grid(axis="y", linestyle="--", alpha=0.4, zorder=0)
        for bar, val in list(zip(bars1, mark_means)) + list(zip(bars2, drop_means)):
            if val > 0:
                ax_ecn.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.3,
                            f"{val:.0f}", ha="center", va="bottom", fontsize=8)
    else:
        ax_ecn.text(
            0.5, 0.5,
            "ECN/drop data not found.\nRe-run parse-results.py after\nat least one 60 s experiment.",
            transform=ax_ecn.transAxes, ha="center", va="center", color="gray", fontsize=9,
        )
        ax_ecn.set_title("Congestion Signal: Marks vs Drops")

    # ── 4. Sender cwnd over time (server-ss.log) OR RTT bars ─────────────
    cwnd_plotted = False
    if latest:
        for m in active_modes:
            row = latest.get(m)
            if not row:
                continue
            samples = load_server_ss_cwnd(results_dir, row["timestamp"], m)
            if samples:
                t_vals, c_vals = zip(*samples)
                ax_cwnd.plot(t_vals, c_vals, label=LABELS[m],
                             color=COLORS[m], linewidth=1.6, alpha=0.9)
                cwnd_plotted = True

    if cwnd_plotted:
        ax_cwnd.set_xlabel("Time (s)")
        ax_cwnd.set_ylabel("cwnd (segments)")
        ax_cwnd.set_title("Sender cwnd Over Time (latest session)")
        ax_cwnd.legend(fontsize=8, loc="lower right")
        ax_cwnd.grid(linestyle="--", alpha=0.4)
    else:
        # Fall back to RTT bar chart from client-side ss
        rtt_vals: dict[str, list[float]] = defaultdict(list)
        for r in rows:
            m = r["mode"]
            if r.get("rtt_mean_ms") not in ("", None):
                rtt_vals[m].append(float(r["rtt_mean_ms"]))

        if any(rtt_vals.values()):
            rtt_means = [np.mean(rtt_vals[m]) if rtt_vals[m] else 0 for m in active_modes]
            rtt_stds  = [np.std(rtt_vals[m]) if len(rtt_vals[m]) > 1 else 0 for m in active_modes]
            bars = ax_cwnd.bar(x, rtt_means, yerr=rtt_stds, capsize=6,
                               color=bar_colors, alpha=0.85, zorder=3)
            ax_cwnd.set_xticks(x); ax_cwnd.set_xticklabels(xlabels)
            ax_cwnd.set_ylabel("RTT (ms)")
            ax_cwnd.set_title("Mean RTT (client-side measurement)")
            ax_cwnd.grid(axis="y", linestyle="--", alpha=0.4, zorder=0)
            for bar, mean in zip(bars, rtt_means):
                if mean > 0:
                    ax_cwnd.text(bar.get_x() + bar.get_width() / 2,
                                 bar.get_height() + 0.3,
                                 f"{mean:.1f} ms", ha="center", va="bottom", fontsize=9)
        else:
            ax_cwnd.text(
                0.5, 0.5,
                "Sender cwnd not yet available.\n\nAdd server-side ss sampling\n"
                "(update run.sh) to see cwnd\nover time per mode.",
                transform=ax_cwnd.transAxes, ha="center", va="center",
                color="gray", fontsize=9, linespacing=1.6,
            )
            ax_cwnd.set_title("Sender cwnd Over Time")
            ax_cwnd.set_axis_off()

    plt.savefig(output, dpi=150, bbox_inches="tight")
    print(f"Saved {output}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Generate comparison charts for AccECN experiment results.")
    parser.add_argument("results", nargs="?", type=Path, default=Path(__file__).parent.parent / "results")
    parser.add_argument("--date", help="Filter summary rows by date prefix, e.g. 20260427")
    parser.add_argument("--output", type=Path, help="Output image path")
    args = parser.parse_args()

    output = args.output or args.results / "comparison.png"
    plot_comparison(args.results, output, args.date)
